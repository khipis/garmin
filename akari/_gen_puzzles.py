    easy = []
    for i in range(15):
        # Wall density rises slightly with index for difficulty curve.
        wd = 0.20 + 0.012 * i
        easy.append(generate(6, wd, seed_base=1000 + i))
    hard = []
    for i in range(15):
        wd = 0.22 + 0.010 * i
        hard.append(generate(7, wd, seed_base=2000 + i))
